#!/usr/bin/env python3
"""Generate the environment (temperature / humidity) icons as LVGL 9 A8 images.

Unlike make_icon.py (which reads dithered PNGs), the two 20x20 glyphs here are
tiny and hand-drawn as ASCII art below, so this tool needs no PIL: '#' is ink
(alpha 255), '.' is paper (alpha 0). Emits ../main/env_icons.c with:
  therm_icon  (20x20)  thermometer, shown left of the temperature readout
  drop_icon   (20x20)  water drop, shown left of the humidity readout

Both are A8 (no color); ui.cpp recolors them black at render time, matching the
cat icon so they read as black dots on the mono reflective panel.
"""
import os

HERE = os.path.dirname(os.path.abspath(__file__))
OUT = os.path.join(HERE, "..", "main", "env_icons.c")

THERM = """\
........#####.......
.......#.....#......
.......#.###.#......
.......#.###.#......
.......#.###.#......
.......#.###.#......
.......#.###.#......
.......#.###.#......
......#..###..#.....
......#.#####.#.....
......#.#####.#.....
......#.#####.#.....
......#.#####.#.....
.......#.....#......
........#####.......
....................
....................
....................
....................
....................
"""

DROP = """\
.........#..........
.........#..........
........#.#.........
........#.#.........
.......#...#........
.......#...#........
......#.....#.......
......#.....#.......
.....#.......#......
.....#.......#......
....#.........#.....
....#....##...#.....
....#...#.....#.....
.....#..#....#......
.....#.......#......
......#.....#.......
.......#...#........
........###.........
....................
....................
"""


def rows(art):
    return [line for line in art.splitlines() if line]


def emit_one(lines, art, symbol):
    r = rows(art)
    h = len(r)
    w = len(r[0])
    for row in r:
        assert len(row) == w, "ragged ASCII art in %s" % symbol
    alpha = []
    for row in r:
        for ch in row:
            alpha.append(255 if ch == "#" else 0)

    lines.append("static const uint8_t %s_map[%d] = {" % (symbol, w * h))
    for y in range(h):
        chunk = alpha[y * w:(y + 1) * w]
        lines.append("    " + ", ".join("0x%02X" % v for v in chunk) + ",")
    lines.append("};")
    lines.append("")
    lines.append("const lv_image_dsc_t %s = {" % symbol)
    lines.append("    .header = {")
    lines.append("        .magic = LV_IMAGE_HEADER_MAGIC,")
    lines.append("        .cf = LV_COLOR_FORMAT_A8,")
    lines.append("        .flags = 0,")
    lines.append("        .w = %d," % w)
    lines.append("        .h = %d," % h)
    lines.append("        .stride = %d," % w)
    lines.append("    },")
    lines.append("    .data_size = %d," % (w * h))
    lines.append("    .data = %s_map," % symbol)
    lines.append("};")
    lines.append("")


def main():
    lines = []
    lines.append("// Generated by tools/make_env_icons.py. Do not edit by hand.")
    lines.append('#include "lvgl.h"')
    lines.append("")
    emit_one(lines, THERM, "therm_icon")
    emit_one(lines, DROP, "drop_icon")
    with open(OUT, "w", encoding="utf-8", newline="\n") as f:
        f.write("\n".join(lines))
    print("wrote", os.path.normpath(OUT))


if __name__ == "__main__":
    main()
